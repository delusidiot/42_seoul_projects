#include "fdf.h"

void	clean_image(t_win *win)
{
	int	i;
	int	*img;

	i = -1;
	img = (int *)win->img->data;
	while (++i < WIN_WIDTH * WIN_HEIGHT)
		img [i] = 0;
}
