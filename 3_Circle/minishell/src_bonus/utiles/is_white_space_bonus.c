#include "minishell_bonus.h"

int	is_white_space(char *str)
{
	int	i;

	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || \
			str[i] == '\v' || str[i] == '\f' || \
			str[i] == '\r' || str[i] == ' ')
		i++;
	return (i);
}
